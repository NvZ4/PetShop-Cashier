    private void AddBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBillActionPerformed
        if(Product.getText().isEmpty() || Quantity.getText().isEmpty() || Price.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information!!");
        }else{
            int total = Integer.valueOf(Price.getText().toString())*Integer.valueOf(Quantity.getText().toString());
            BillTable.setValueAt(n, row, 0);
            BillTable.setValueAt(Product.getText(), row, 1);
            BillTable.setValueAt(Price.getText(), row, 2);
            BillTable.setValueAt(Quantity.getText(), row, 3);
            BillTable.setValueAt(total, row, 4);
            GrandTot = GrandTot + total;
            GrandTotal.setText("Total"+GrandTot);
            n++;
            row++;
            UpdateQuantity();
        }
        
    }//GEN-LAST:event_AddBillActionPerformed
