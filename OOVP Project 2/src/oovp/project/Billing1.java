    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
               if (Customers.getSelectedIndex() == -1 || UserID.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else
            try {
                CountIt();
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/petshopproject", "root", "");
                PreparedStatement Save = (PreparedStatement) Con.prepareStatement("Insert into bill value(?,?,?,?)");
                Save.setInt(1, ItemId);
                Save.setInt(2, Integer.valueOf(Customers.getSelectedItem().toString()));
                Save.setInt(3, Integer.valueOf(UserID.getSelectedItem().toString()));
                Save.setInt(4, GrandTot);
                int row = Save.executeUpdate();
                JOptionPane.showMessageDialog(this, "Pets Added");
                Con.close();
                DisplayProduct();
                Clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
    }//GEN-LAST:event_SaveBtnActionPerformed

