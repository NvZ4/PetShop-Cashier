    private void CustEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustEditActionPerformed

        if (CustName.getText().isEmpty() || CustAddress.getText().isEmpty() || CustPhone.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else
            try {
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/petshopproject", "root", "");
                PreparedStatement Save = (PreparedStatement) Con.prepareStatement("update customers set CustName=?, CustAddress=?, CustPhone=? where CustID=?");
                Save.setInt(4, key);
                Save.setString(1, CustName.getText());
                Save.setString(2, CustAddress.getText());
                Save.setString(3, CustPhone.getText());
                int row = Save.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Updated");
                Con.close();
                DisplayCustomers();
                Clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
    }//GEN-LAST:event_CustEditActionPerformed

