    int ItemId;
    Statement st1 = null;
    ResultSet rs1 = null;
    private void CountIt(){
        try{
            st1 = (Statement) Con.createStatement();
            rs1 = st1.executeQuery("select Max(CustID) from customers");
            rs1.next();
            ItemId = rs1.getInt(1)+1;
        }
        catch (Exception e) {
   
        }
    }
    private void CustSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustSaveActionPerformed
        if (CustName.getText().isEmpty() || CustAddress.getText().isEmpty() || CustPhone.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else
            try {
                CountIt();
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/petshopproject", "root", "");
                PreparedStatement Save = (PreparedStatement) Con.prepareStatement("Insert into customers value(?,?,?,?)");
                Save.setInt(1, ItemId);
                Save.setString(2, CustName.getText());
                Save.setString(3, CustAddress.getText());
                Save.setString(4, CustPhone.getText());
                int row = Save.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Added");
                Con.close();
                DisplayCustomers();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
    }//GEN-LAST:event_CustSaveActionPerformed
